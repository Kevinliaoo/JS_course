// A loop is something that is repeated

// Defined: Limited ammount of repetitions

// Undefined: Unlimited """"

// The main purpose of using loops is to avoid repetition of code

// For a variable i that is equals to 0, repeat the code WHILE the variable i is less than 10,
// when you finish the execution, add 1 to the variable i (counter)

// While programming, we ALWAYS start counting from 0

// The i is a counter
for (let i = 0; i <= 10; i++) {
	// Code to be repeated
	console.log(i);

	// At the end of the loop, i is incremented by 1
}

